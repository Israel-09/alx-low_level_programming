
/**
 * add - sums up two integers
 * @a: the first integer.
 * @b: the second integer
 * Return: the sum of both integers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - finds the difference between two integers
 * @a: the first integer.
 * @b: the second integer
 * Return: the different between both integers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - finds the product of two integers
 * @a: the first integer.
 * @b: the second integer
 * Return: the product of both integers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides a from b
 * @a: the first integer.
 * @b: the second integer
 * Return: the divion of both integers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - remainder of two integers
 * @a: the first integer.
 * @b: the second integer
 * Return: the remainder of the division
 * between both integers
 */
int mod(int a, int b)
{
	return (a % b);
}
